def get_tarot_system_prompt(theme: str | None) -> str:
    base = TAROT_SYSTEM_PROMPT
    hint = TAROT_THEME_HINTS.get(theme or "", "")
    if hint:
        return f"{base}\n- テーマ: {hint}"
    return base
